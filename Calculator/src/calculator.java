    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // 1. Ambil dulu tulisan yang ada di layar
        String tulisan = jLabel1.getText();

        // 2. Cek dulu: Apakah layarnya ada isinya? (Panjangnya lebih dari 0)
        // PENTING: Kalau kita coba hapus saat layar kosong, program bisa error (Crash)!
        if (tulisan.length() > 0) {

            // 3. Potong tulisan dari index 0 (depan) sampai panjangnya dikurangi 1 (belakang dibuang)
            String tulisanBaru = tulisan.substring(0, tulisan.length() - 1);

            // 4. Tampilkan tulisan hasil potongan tadi
            jLabel1.setText(tulisanBaru);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

