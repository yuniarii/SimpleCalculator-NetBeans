    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        String teksLayar = jLabel1.getText();

    if (!teksLayar.isEmpty()) {
        try {
            // Kita gunakan alat bantu Scanner untuk membaca angka dan operator satu-satu
            // Kita sisipkan spasi di sekitar operator agar mudah dibaca oleh Scanner
            String rumus = teksLayar.replace("+", " + ")
                                    .replace("-", " - ")
                                    .replace("x", " * ")
                                    .replace("/", " / ");
            
            java.util.Scanner sc = new java.util.Scanner(rumus);
            
            // Ambil angka pertama sebagai modal awal
            double hasil = sc.nextDouble();
            
            // Selama masih ada operator dan angka selanjutnya, hitung terus!
            while (sc.hasNext()) {
                String operator = sc.next();
                double angkaBerikutnya = sc.nextDouble();
                
                if (operator.equals("+")) hasil += angkaBerikutnya;
                else if (operator.equals("-")) hasil -= angkaBerikutnya;
                else if (operator.equals("*")) hasil *= angkaBerikutnya;
                else if (operator.equals("/")) {
                    if (angkaBerikutnya != 0) hasil /= angkaBerikutnya;
                    else { jLabel1.setText("Error: Div 0"); return; }
                }
            }

            // Tampilkan hasil akhir
            if (hasil % 1 == 0) {
                jLabel1.setText(String.valueOf((int) hasil));
            } else {
                jLabel1.setText(String.valueOf(hasil));
            }
            
        } catch (Exception e) {
            // Jika user ngetik aneh-aneh (misal 5++5), maka error
            jLabel1.setText("Error");
        }
    }
    }//GEN-LAST:event_jButton15ActionPerformed

